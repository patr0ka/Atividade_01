// Existem três tipos de passagem de parâmetros, sendo elas;

// Por valor: Por mais que o valor do argumento seja passado para o 
// parâmetro, o valor em si não sofre alterações ao alterar o parâmetro;

// Por refêrencia: Diferente da por valor, essa ferramento faz com que
// ocorram alterações no valor da variavel ao chamar a função;

// Por ponteiro: Parecido com o método por referência, o por ponteiro 
// também modifica o valor da variavel, porém, usando ferramentas de 
// ponteiro, como por exemplo, *a para acessar o conteudo da variavel,
// e &a para acessar o endereço da variavel.

